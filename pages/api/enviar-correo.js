import nodemailer from "nodemailer";
import JsBarcode from "jsbarcode";
import { createCanvas } from "canvas";
import { connectToDatabase, sql } from "../../dbconfig";

export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ message: "M√©todo no permitido" });
  }

  console.log("üì© Recibiendo datos para enviar correo:", req.body);
  try {
    let { Origen, Destino, NumeroPedido, Producto } = req.body;

    Origen = Origen.toString();
    Destino = Destino.toString();

    if (!Origen || !Destino || !NumeroPedido || !Array.isArray(Producto) || Producto.length === 0) {
      console.error("‚ùå Datos inv√°lidos:", { Origen, Destino, NumeroPedido, Producto });
      return res.status(400).json({ message: "Datos inv√°lidos en la petici√≥n" });
    }

    const pool = await connectToDatabase();

    console.log("üîç Obteniendo datos del almac√©n y correo del usuario asignado al origen...");

    const query = `
      SELECT DISTINCT u.Correo, 
                      a_origen.CNOMBREALMACEN AS NombreAlmacenOrigen, 
                      a_destino.CNOMBREALMACEN AS NombreAlmacenDestino 
      FROM UsuariosLocal AS u
      JOIN admAlmacenes AS a_origen ON a_origen.CCODIGOALMACEN = @Origen
      JOIN admAlmacenes AS a_destino ON a_destino.CCODIGOALMACEN = @Destino
      WHERE u.Clave = a_origen.CNOMBREALMACEN 
        AND u.Correo IS NOT NULL;
    `;

    console.log("üîç Query:", query);

    const result = await pool
      .request()
      .input("Origen", sql.VarChar, Origen)
      .input("Destino", sql.VarChar, Destino)
      .query(query);

    console.log("üìú Resultado de la consulta:", result.recordset);

    if (result.recordset.length === 0) {
      console.error("‚ùå No se encontr√≥ un usuario con correo asignado al almac√©n de origen.");
      return res.status(404).json({ message: "No hay usuario v√°lido para enviar el correo" });
    }

    const { Correo, NombreAlmacenOrigen, NombreAlmacenDestino } = result.recordset[0];

    console.log(`‚úÖ Correo encontrado: ${Correo}`);

    console.log("üñ® Generando c√≥digo de barras...");
    let barcodeImage = "";
    try {
      const canvas = createCanvas(200, 50);
      JsBarcode(canvas, NumeroPedido, { format: "CODE128", displayValue: true, fontSize: 14, textMargin: 4 });
      barcodeImage = canvas.toDataURL();
    } catch (barcodeError) {
      console.error("‚ö†Ô∏è Error generando c√≥digo de barras, pero se continuar√° con el correo:", barcodeError);
    }

    const productosHTML = Producto.map(
      (prod) => `<li><strong>${prod.NombreProducto}</strong> - ${prod.Unidades} unidades</li>`
    ).join("");

    const mailOptions = {
      from: `"Sistema de Traspasos" <${process.env.EMAIL_USER}>`,
      to: Correo,
      subject: `Orden de Surtir Traspaso - ${NumeroPedido}`,
      html: `
        <h1>Orden de Surtir Traspaso</h1>
        <p><strong>Origen:</strong> ${NombreAlmacenOrigen}</p>
        <p><strong>Destino:</strong> ${NombreAlmacenDestino}</p>
        <p><strong>N√∫mero de Traspaso:</strong> ${NumeroPedido}</p>
        <h2>Productos</h2>
        <ul>${productosHTML}</ul>
        ${barcodeImage ? `<p><strong>C√≥digo de Barras del Traspaso:</strong></p><img src="${barcodeImage}" alt="C√≥digo de Barras">` : ""}
      `,
    };

    const transporter = nodemailer.createTransport({
      host: process.env.SMTP_HOST || "smtp.gmail.com",
      port: Number(process.env.SMTP_PORT) || 465,
      secure: process.env.SMTP_SECURE === "true",
      auth: {
        user: process.env.EMAIL_USER,
        pass: process.env.EMAIL_PASS,
      },
      tls: { rejectUnauthorized: false },
    });

    console.log("üì® Intentando enviar correo...");
    try {
      const info = await transporter.sendMail(mailOptions);
      console.log(`‚úÖ Correo enviado con √©xito a ${Correo} (${info.messageId})`);

      console.log("üîÑ Llamando a la API de actualizaci√≥n de almac√©n...");

      const apiUrl = process.env.NEXT_PUBLIC_API_URL || "http://localhost:3000";
      const updateResponse = await fetch(`${apiUrl}/api/Actualizar-almacen`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ NumeroPedido })
      });

      const updateResult = await updateResponse.json();
      console.log("üì¶ Resultado de la actualizaci√≥n de almac√©n:", updateResult);

      return res.status(200).json({ message: "Correo enviado y almac√©n actualizado con √©xito" });
    } catch (emailError) {
      console.error("‚ùå Error enviando correo:", emailError);
      return res.status(500).json({ message: "Error enviando el correo", error: emailError.message });
    }
  } catch (error) {
    console.error("‚ùå Error en enviar-correo.js:", error);
    return res.status(500).json({ message: "Error interno del servidor", error: error.message });
  }
}
